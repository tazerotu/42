#include <unistd.h>

int main (int ac, char **av)
{
    if(ac < 2)
        return 0;
    else if (ac > 2)
        return 0;
    else
    {
        int i = 0;
        while(av[1][i] == ' ')
            i++;
        while(av[1][i] != ' ' && av[1][i] != '\0')
        {
            write(1, &av[1][i], 1);
            i++;
        }
        write(1, "\n", 1);        
    }
    return 0;
}